function translate(value: string) {
  let result = value;
  const entries = Object.entries(dictionary).sort(([left], [right]) => right.length - left.length);
  for (let pass = 0; pass < 3; pass += 1) {
    const before = result;
    const trimmed = result.trim();
    if (exactFallback[trimmed]) result = result.replace(trimmed, exactFallback[trimmed]);
    result = entries.reduce((text, [from, to]) => text.replaceAll(from, to), result);
    if (result === before) break;
  }
  return result;
}

function translateDom() {
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);
  const nodes: Text[] = [];
  let node: Node | null;
  while ((node = walker.nextNode())) nodes.push(node as Text);
  nodes.forEach((textNode) => {
    if (!textNode.nodeValue?.trim()) return;
    if (textNode.parentElement?.closest("script, style, [data-no-translate]")) return;
    const translated = translate(textNode.nodeValue);
    if (translated !== textNode.nodeValue) textNode.nodeValue = translated;
  });
  document.querySelectorAll<HTMLElement>("[placeholder], [title], [aria-label]").forEach((element) => {
    ["placeholder", "title", "aria-label"].forEach((attribute) => {
      const value = element.getAttribute(attribute);
      if (value) {
        const translated = translate(value);
        if (translated !== value) element.setAttribute(attribute, translated);
      }
    });
  });
  document.querySelectorAll<HTMLInputElement | HTMLTextAreaElement>("input, textarea").forEach((element) => {
    const translated = translate(element.value);
    if (translated !== element.value && /[\u4e00-\u9fff]/.test(element.value)) {
      element.value = translated;
    }
  });
}

export function useLanguage() {
  const [language, setLanguage] = useState<Language>(() => (
    typeof window !== "undefined" && window.localStorage.getItem("mcp-control-language") === "en" ? "en" : "zh"
  ));
  useEffect(() => {
    document.documentElement.lang = language === "en" ? "en" : "zh-CN";
    if (language !== "en") return;
    let applying = false;
    const apply = () => {
      if (applying) return;
      applying = true;
      translateDom();
      applying = false;
    };
    apply();
    const interval = window.setInterval(apply, 250);
    return () => {
      window.clearInterval(interval);
    };
  }, [language]);
  const changeLanguage = (next: Language) => {
    window.localStorage.setItem("mcp-control-language", next);
    setLanguage(next);
    if (next === "zh") window.location.reload();
  };
  return { language, changeLanguage };
}

export function LanguageToggle() {
  const { language, changeLanguage } = useLanguage();
  return (
    <button
      className="language-toggle"
      onClick={() => changeLanguage(language === "zh" ? "en" : "zh")}
      aria-label={language === "zh" ? "切换为英文" : "Switch to Chinese"}
      title={language === "zh" ? "切换为英文" : "Switch to Chinese"}
    >
      {language === "zh" ? "EN" : "ZH"}
    </button>
  );
}
